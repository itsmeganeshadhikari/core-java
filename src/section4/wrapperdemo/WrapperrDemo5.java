package section4.wrapperdemo;

public class WrapperrDemo5 {

	public static void main(String[] args) {
		System.out.println(Integer.MAX_VALUE);
		System.out.println(Integer.MIN_VALUE);
		System.out.println(Integer.SIZE);
		System.out.println(Integer.BYTES);
		System.out.println(Integer.toBinaryString(23));
		System.out.println(Integer.toHexString(23));
		System.out.println(Integer.toOctalString(45));
		System.out.println(Integer.valueOf("10111", 2));
		System.out.println(Integer.max(4, 5));
	}
}
