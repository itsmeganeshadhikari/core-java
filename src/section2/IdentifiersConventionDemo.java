package section2;

//1.class convention: First letter of each word should be 
//in capital letters
public class IdentifiersConventionDemo {

	// 2.variable convention:each letter of first word should be
	// in small letters and after that First letter of each word
	// should be in capital letters
	int anyVarDemo;

	// 3.method convention:each letter of first word should be
	// in small letters and after that First letter of each word
	// should be in capital letters

	public void sayHello() {
		System.out.println("helllo");
	}

	// 4.constant convention:Every letter of each words should be in 
	// capital letters but each words should be concatenated with
	// underscore
	final double VALUE_OF_PI = 3.14;
}
